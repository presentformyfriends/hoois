class Lookup < ApplicationService

    require "addressable/uri"


    def initialize(params)
        @lookup = params[:lookup].strip # Get user-submitted Lookup data from params, strip all whitespace
    end

    def call
  
        tainted_or_no = @lookup.tainted?

        # Remove illegal characters
        gsub_lookup = @lookup.gsub(/ |'|/, "")

        # Extract domain from lookup
        begin

            # Test string against Regex pattern for IPV4 (1.1.1.1) and IPV6 (2001:db8::1428:57ab) addresses
            if gsub_lookup.match?(/((^\s*((([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])\.){3}([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5]))\s*$)|(^\s*((([0-9A-Fa-f]{1,4}:){7}([0-9A-Fa-f]{1,4}|:))|(([0-9A-Fa-f]{1,4}:){6}(:[0-9A-Fa-f]{1,4}|((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9A-Fa-f]{1,4}:){5}(((:[0-9A-Fa-f]{1,4}){1,2})|:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9A-Fa-f]{1,4}:){4}(((:[0-9A-Fa-f]{1,4}){1,3})|((:[0-9A-Fa-f]{1,4})?:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){3}(((:[0-9A-Fa-f]{1,4}){1,4})|((:[0-9A-Fa-f]{1,4}){0,2}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){2}(((:[0-9A-Fa-f]{1,4}){1,5})|((:[0-9A-Fa-f]{1,4}){0,3}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){1}(((:[0-9A-Fa-f]{1,4}){1,6})|((:[0-9A-Fa-f]{1,4}){0,4}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(:(((:[0-9A-Fa-f]{1,4}){1,7})|((:[0-9A-Fa-f]{1,4}){0,5}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:)))(%.+)?\s*$))/)
            
                clean_lookup = gsub_lookup

            else

                domain = Addressable::URI.parse(gsub_lookup).domain

                if domain.present?

                    clean_lookup = domain
    
                else
    
                    # Remove scheme + subdomain if subdomain is 'www' (removes 'https://', 'http://', 'https://www.', 'http://www.')
                    stripped_lookup = gsub_lookup.gsub(/(https?:\/\/)?(www\.)?/, "")
    
                    # Remove trailing slash if any
                    clean_lookup = stripped_lookup.chomp("/")
    
                end

            end

            return clean_lookup

        rescue Addressable::URI::InvalidURIError, Whois::NoInterfaceError, Whois::AllocationUnknown

            # Rescued by Addressable or Whois Error, return nil
            clean_lookup = nil
            return clean_lookup
        
        end

    end
    
end
